# Python里支持很多运算符
# +加  -减  *乘  /除  **幂运算  //整除  %取余
print(1 + 1)  #2
print(2 - 1)  #1
print(3 * 2)  #6

#在Python里两个整数相除得到一个浮点数
print(6 / 2)  #3.0
print(9 / 2)  #4.5

print(2 ** 6) #64
print(81 ** 1 / 2) #40.5  幂运算优先级高于除
print(81 ** (1 / 2))# 9.0

print(10 / 3)  #3.3333333333333335
print(10 // 3) #3  取先下整数部分
print((-5) // 2)#-3
print(10 % 3)  #1  取余数部分


