# 数学计算相关的模块
import math

print(math.factorial(6))  # 720 阶乘

print(math.floor(12.99))  # 12  向下取整
print(math.ceil(15.001))  # 16  向上取整

print(math.pow(2, 10))  # 幂运算 1024

# round() 内置函数，实现四舍五入到指定位数

print(math.sin(math.pi/6))  # 弧度制  π=180°
print(math.cos(math.pi/3))  # 弧度制  π=180°
print(math.tan(math.pi/3))  # 弧度制  π=180°
