# 变量.title()  将字符串中字母开头大写
# 变量.upper()  将字符串中所有字母大写
# 变量.lower()  将字符串中所有字母小写

name = 'ananna233 like Eat MuTTon sHaSHLIK.'
print(name.title())  #Ananna233 Like Eat Mutton Shashlik.
print(name.upper())  #ANANNA233 LIKE EAT MUTTON SHASHLIK.
print(name.lower())  #ananna233 like eat mutton shashlik.
print(name)  # 这种改变是暂时的