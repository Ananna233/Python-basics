a = 10
b = 'hello'
c = True
d = ['张三','李四','ananna233']

#使用type内置类可以查看一个变量对应的类型
print(type(a)) #<class 'int'> 整数型
print(type(b)) #<class 'str'> 字符串类型
print(type(c)) #<class 'bool'> 布尔类型
print(type(d)) #<class 'list'>列表类型

#<class 'float'> 浮点类型
#<class 'tuple'> 元组类型
#<class 'dict'> 字典类型
#<class 'set'> 集合类型
