# 标识符：变量 模块名 函数名 类名
# 标识符的命名规则与规范
# 1.由数字、字母和_组成，不能以数字开头
# 2.严格区分大小写（计算机编程里，一共有52个英文字母）
# 3.不能使用关键字  （关键字：在Python语言里，有特殊含义对的单词，例如 if、for、else）

# 规范：建议遵守，遵守规范会显得专业并且代码易读
# 1.顾名思义
# 2.遵守一定的命名规范
#  （1）.小驼峰命名法：第一个单词的首字母小写，以后每个单词的首字母都大写  例：useNameAndPassword （C语言常用）
#  （2）.大驼峰命名法：每一个单词的首字母都大写 例PersonMldel
#  （3）.使用下划线链接：例 user_name_and_password
#在Python里的变量、函数和模块名使用下划线链接；Python里的类名使用大驼峰命名法

